    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Obtener los datos de los JTextField
        String nombreOrquidea = txtNombreOrquidea.getText();
        String especie = txtEspecie.getText();

        // Verificar que no estén vacíos los campos
        if (nombreOrquidea.isEmpty() || especie.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
        } else {
            // Verificar que se haya seleccionado una orquídea
            int filaSeleccionada = TablaOrquideas.getSelectedRow();
            if (filaSeleccionada != -1) {
                // Obtener el ID de la orquídea seleccionada (suponiendo que el ID está en la columna 0)
                int idOrquideaSeleccionada = (int) TablaOrquideas.getValueAt(filaSeleccionada, 0);

                // Deshabilitar el botón de editar antes de hacer la edición
                jButton2.setEnabled(false);

                // Llamar al controlador para editar la orquídea
                OrquideaController orquideaController = new OrquideaController();
                boolean resultado = orquideaController.editarOrquidea(idOrquideaSeleccionada, nombreOrquidea, especie);

                if (resultado) {
                    JOptionPane.showMessageDialog(null, "Orquídea actualizada exitosamente.");
                    // Limpiar los campos después de la actualización
                    txtNombreOrquidea.setText("");
                    txtEspecie.setText("");

                    // Recargar la tabla con los datos actualizados
                    cargarOrquideasEnTabla();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al actualizar la orquídea.");
                }

                // Habilitar el botón después de la edición, en caso de éxito o error
                jButton2.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "Por favor, seleccione una orquídea para editar.");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // Obtener los datos del JTextField
        String nombreOrquidea = txtNombreOrquidea.getText();
        String especie = txtEspecie.getText();

        // Verificar que no estén vacíos los campos
        if (nombreOrquidea.isEmpty() || especie.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
        } else {
            // Llamar al controlador para insertar la orquídea
            OrquideaController orquideaController = new OrquideaController();
            boolean resultado = orquideaController.insertarOrquidea(nombreOrquidea, especie);

            if (resultado) {
                JOptionPane.showMessageDialog(null, "Orquídea insertada exitosamente.");
                // Limpiar los campos después de la inserción
                txtNombreOrquidea.setText("");
                txtEspecie.setText("");

                // Opcional: Recargar la tabla si la tienes en la interfaz
                cargarOrquideasEnTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error al insertar la orquídea.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTabbedPane1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MouseClicked
  
// Verificar qué pestaña fue seleccionada (puedes usar getSelectedIndex() para obtener el índice)
int index = jTabbedPane1.getSelectedIndex();

// Verificar si el índice corresponde a la pestaña que quieres
if (index == 1) { // Si es la primera pestaña (índice 1) para Condición Ambiental
    new VistaCondicionAmbiental().setVisible(true); // Muestra la vista de Condición Ambiental
} else if (index == 2) { // Si es la segunda pestaña (índice 2) para Alertas
    new VistaAlertas().setVisible(true); // Muestra la vista de Alertas
} else if (index == 3) { // Si es la tercera pestaña (índice 3) para Sensores
    new VistaSensor().setVisible(true); // Muestra la vista de Sensor
}


    }//GEN-LAST:event_jTabbedPane1MouseClicked

