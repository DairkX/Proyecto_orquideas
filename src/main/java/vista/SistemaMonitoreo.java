public void cargarOrquideasEnTabla() {
    OrquideaController orquideaController = new OrquideaController();
    List<Orquidea> orquideas = orquideaController.obtenerOrquideas();
    
    DefaultTableModel modelo = (DefaultTableModel) TablaOrquideas.getModel();
    modelo.setRowCount(0); // Limpiar la tabla antes de agregar nuevos datos

    for (Orquidea orquidea : orquideas) {
        modelo.addRow(new Object[]{orquidea.getIdOrquidea(), orquidea.getNombreOrquidea(), orquidea.getEspecie()});
    }
}



